let day = "Sun"

switch(day){
    case "M":{
        console.log("Monday")
        break;
    }
    case "Tu":{
        console.log("Tuesday")
        break;
    }
    case "Wed":{
        console.log("Wednesady")
        break;
    }
    case "Th":{
        console.log("Thrusday")
        break;
    }
    case "Fri":{
        console.log("Friday")
        break;
    }
    case "Sat":{
        console.log("Saturday")
        break;
    }
    case "Sun":{
        console.log("Sunday")
        break;
    }
    default :{
        console.log("Invalid Day")
        break;
    }

}
